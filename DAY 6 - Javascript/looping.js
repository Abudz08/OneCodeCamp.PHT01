// Control Flow Structure

//Looping statement

//For loop
for (let i=1; i <= 5; i++){
    console.log("For loop count:", i)
}

//While loop
let count = 1;

while(count <= 5){
    console.log("While loop count:", count);
    count++;
}

//Do-While loop
let a = 1;

do {
    console.log("Do-While count:", a);
    a++;
} while (a <= 5);